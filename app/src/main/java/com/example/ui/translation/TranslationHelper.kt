package com.example.ui.translation

enum class AppLanguage(val code: String, val displayName: String, val flag: String, val isRtl: Boolean) {
    ENGLISH("en", "English", "🇬🇧", false),
    FRENCH("fr", "Français", "🇫🇷", false),
    ARABIC("ar", "العربية", "🇸🇦", true),
    GERMAN("de", "Deutsch", "🇩🇪", false),
    DANISH("da", "Dansk", "🇩🇰", false),
    SWEDISH("sv", "Svenska", "🇸🇪", false),
    SPANISH("es", "Español", "🇪🇸", false);

    companion object {
        fun fromCode(code: String): AppLanguage {
            return values().find { it.code == code } ?: ENGLISH
        }
    }
}

object TranslationHelper {
    fun translate(key: String, lang: AppLanguage): String {
        return translations[lang]?.get(key) ?: translations[AppLanguage.ENGLISH]?.get(key) ?: key
    }

    fun translateSymptom(symptom: String, lang: AppLanguage): String {
        return symptomTranslations[lang]?.get(symptom) ?: symptom
    }

    fun translateMood(mood: String, lang: AppLanguage): String {
        return moodTranslations[lang]?.get(mood) ?: mood
    }

    fun translateSleepQuality(quality: String, lang: AppLanguage): String {
        return sleepQualityTranslations[lang]?.get(quality) ?: quality
    }
    
    fun translateMonth(monthIndex: Int, lang: AppLanguage): String {
        val months = monthTranslations[lang] ?: monthTranslations[AppLanguage.ENGLISH]!!
        return if (monthIndex in 0..11) months[monthIndex] else ""
    }

